void _AUD_GetStrInfo(SdSound_MelodyMode_k eMelodyMode, MEM_BUFFER_INFO_T *prMemBuf)
{
    VERIFY(prMemBuf != NULL);

    switch (eMelodyMode)
    {
    case SD_MELODY_POWER_OFF:    
        prMemBuf->pData = (UINT8 *)_au1TablePowerOff;
        prMemBuf->u4Length = sizeof(_au1TablePowerOff);
        break;
    case SD_MELODY_POWER_ON:    
        prMemBuf->pData = (UINT8 *)_au1TablePowerOn;
        prMemBuf->u4Length = sizeof(_au1TablePowerOn);
        break;
    case SD_MELODY_POWER_OFF_NOGAIN:    
        prMemBuf->pData = (UINT8 *)_au1TablePowerOff;
        prMemBuf->u4Length = sizeof(_au1TablePowerOff);
        break;
    case SD_MELODY_POWER_ON_NOGAIN:    
        prMemBuf->pData = (UINT8 *)_au1TablePowerOn;
        prMemBuf->u4Length = sizeof(_au1TablePowerOn);
        break;
    case SD_MELODY_FAST_POWER_ON:    
        prMemBuf->pData = (UINT8 *)_au1TableFastPowerOn;
        prMemBuf->u4Length = sizeof(_au1TableFastPowerOn);
        break; 
    case SD_MELODY_FAST_POWER_ON_NOGAIN:
        prMemBuf->pData = (UINT8 *)_au1TableFastPowerOn;
        prMemBuf->u4Length = sizeof(_au1TableFastPowerOn);
        break; 
    default:
        prMemBuf->pData = (UINT8 *)_au1TablePowerOn;
        prMemBuf->u4Length = sizeof(_au1TablePowerOff);
        break;
    }
    
}



