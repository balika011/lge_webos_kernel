/*
 *  sha1.h
 *
 *  Description:
 *      This is the header file for code which implements the Secure
 *      Hashing Algorithm 1 as defined in FIPS PUB 180-1 published
 *      April 17, 1995.
 *
 *      Many of the variable names in this code, especially the
 *      single character names, were used because those were the names
 *      used in the publication.
 *
 *      Please read the file sha1.c for more information.
 *
 */

#ifndef _SHA256_H_
#define _SHA256_H_

#include <stdint.h>
/*
 * If you do not have the ISO standard stdint.h header file, then you
 * must typdef the following:
 *    name              meaning
 *  uint32_t         unsigned 32 bit integer
 *  uint8_t          unsigned 8 bit integer (i.e., unsigned char)
 *  int_least16_t    integer of >= 16 bits
 *
 */

#ifndef _SHA_enum_
#define _SHA_enum_
enum
{
    shaSuccess = 0,
    shaNull,            /* Null pointer parameter */
    shaInputTooLong,    /* input data too long */
    shaStateError       /* called Input after Result */
};
#endif

#define SHA256_HASH_SIZE    (32)

#define SHA1HashSize 20

// This structure will hold context information for the SHA-256
// hashing operation
typedef struct
{
    uint32_t Intermediate_Hash[SHA256_HASH_SIZE/4];   /* Message Digest  */

    uint32_t Length_Low;                              /* Message length in bits      */
    uint32_t Length_High;                             /* Message length in bits      */

                                                    /* Index into message block array   */
    uint32_t Message_Block_Index;
    uint8_t Message_Block[64];                        /* 512-bit message blocks      */

    int Computed;                                 /* Is the digest computed?         */
    int Corrupted;                                /* Is the message digest corrupted? */
} SHA256_CONTEXT_T;

/*
 *  Function Prototypes
 */

int SHA256_Reset(SHA256_CONTEXT_T *);
int SHA256_Input(SHA256_CONTEXT_T *, const uint8_t *, uint32_t);
int SHA256_Result(SHA256_CONTEXT_T *, uint8_t Message_Digest[SHA256_HASH_SIZE]);
int SHA256(const uint8_t *message_array, uint32_t length, uint8_t Message_Digest[SHA256_HASH_SIZE]);
int mtk_hmac_sha256(uint8_t * key, uint32_t key_size, uint8_t * msg, uint32_t msg_size,  uint8_t result[SHA256_HASH_SIZE]);

#endif
