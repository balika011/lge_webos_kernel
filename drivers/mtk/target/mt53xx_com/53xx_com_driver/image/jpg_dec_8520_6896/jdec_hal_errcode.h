#ifndef _HAL_JDEC_ERRCODE_H_
#define _HAL_JDEC_ERRCODE_H_


#define JDEC_HAL_UOKCODE(group, okcode)     \
  ((INT32)                                  \
    ((UINT32)(0x00000000) |                 \
           (UINT32)((group & 0xff) << 8) |  \
           (UINT32)(okcode & 0xff))         \
  )


#define JDEC_HAL_UERRCODE(group, errcode)   \
  ((INT32)                                  \
    ((UINT32)(0x80000000) |                 \
           (UINT32)((group & 0xff) << 8) |  \
           (UINT32)(errcode & 0xff))        \
  )


#define JDEC_HAL_GROUP_GENERAL             0


#define S_JDEC_HAL_OK                  JDEC_HAL_UOKCODE(JDEC_HAL_GROUP_GENERAL,0x00)

#define E_JDEC_HAL_FAIL                JDEC_HAL_UERRCODE(JDEC_HAL_GROUP_GENERAL,0x00)
#define E_JDEC_HAL_DECODING            JDEC_HAL_UERRCODE(JDEC_HAL_GROUP_GENERAL, 0x01)



#endif
