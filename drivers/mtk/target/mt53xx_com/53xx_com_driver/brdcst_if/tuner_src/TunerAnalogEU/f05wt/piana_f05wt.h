/** @file piana_dtos40a.h
 *  Analog PI driver header file of Samsung DTOS40A combo tuner.

****************************************************************************/

#ifndef _DRV_TUNER_H_
#define _DRV_TUNER_H_

//-----------------------------------------------------------------------------
// Configurations
//-----------------------------------------------------------------------------
  #define TUNER_ONE_ADDR_TUNER       0xC2 //left open
  #define TUNER_ONE_ADDR_IF          0x86

  #define F05WT_ANA_PIC_IF      38900       // Khz
  #define F05WT_ANA_FREQ_STEP   500         // 100hz
  #define F05WT_ANA_UNLOCK      0
  #define F05WT_ANA_LOCK        1

  /** TunerDev Parameters */
  #define SCAN_DIRECTION_DEC                0
  #define SCAN_DIRECTION_INC                1
  
  #define TV_FINE_TUNE_THRESHOLD  0x5A    //  50KHz*90 = 4.5MHz
  
  #define TV_FINE_TUNE_THRESHOLD_KHZ  4500    // -2.25MHz ~  +2.25MHz
  
  #define SCAN_TINY_STEP          0x02    // 125khz
  #define SCAN_SMALL_STEP         0x04    // 250khz
  #define SCAN_MIDDLE_STEP        0x08    // 500khz
  #define SCAN_LARGE_STEP         0x10    // 1.0Mhz
  #define SCAN_QUICK_STEP         0x20    // 2.0Mhz
  #define SCAN_NEXT_CH_STEP       0x30    // 3.0Mhz

  enum
  {
    TUNER_PLL_LOCK_STATE = 0,
    TUNER_VIF_LOCK_STATE
  };
  
  #endif /* __DRV_TUNER_H__ */
  
