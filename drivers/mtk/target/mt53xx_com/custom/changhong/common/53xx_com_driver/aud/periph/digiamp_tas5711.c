{
	UINT8 * Pstr=NULL;
	UINT8  DataLength = 0;
	UINT8  ProductTypeIndex=0;
	ADAC_ResetAmp();
	ProductTypeIndex=GetCurrentCHProductTypeIndex();
#if !defined(ENABLE_CHANGHONG_PCB_MTK)
    switch(ProductTypeIndex)
    {
 
	case TYPE_3D42B4000i:
	case TYPE_3D47B4000i:
	case TYPE_3D50B4000i:
		Pstr = AmpInitTbl_2_0_3D50B4000i;
	break;
	case TYPE_3D47B8000i:
		Pstr = AmpInitTbl_2_0_3D50B4000i;
	break;	

	case TYPE_3D55B4000i:
		Pstr = AmpInitTbl_2_0_3D55B4000i;
	break;
	case TYPE_3D55B8000i:
		Pstr = AmpInitTbl_2_0_3D55B4000i;
	break;

	default:
		Pstr = AmpInitTbl_2_0_3D50B4000i; // 2.0-default
	break;     
     
    }
#else
    Pstr = AmpInitTbl_2_0_default;   
#endif

	  do
        {
            DataLength = *Pstr;
            if(DataLength>20)
            {
                printf("5711 set command error!!\n");
                break;
            }
            g_ucTas5711RegAddr = *(++Pstr); 
            Pstr++;

			_TAS5711_RegWriteN(&g_ucTas5711RegAddr,DataLength,Pstr);

            if (g_ucTas5711RegAddr==0x1B||g_ucTas5711RegAddr==0x46||g_ucTas5711RegAddr==0x50)
            {
				x_thread_delay(60);	
            }
            else// if (i<=5)
            {
				x_thread_delay(1); 
            }
            Pstr = Pstr + DataLength;
        }while(*Pstr != 0);
}

